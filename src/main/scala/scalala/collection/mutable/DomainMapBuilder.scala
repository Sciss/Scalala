package scalala;
package collection;
package mutable;

/**
 * Builds a DomainMap of type To after being given a series of
 * key, value pairs.
 *
 * @author dramage
 */
trait DomainMapBuilder[@specialized(Int,Long) A, @specialized(Int,Long,Float,Double) B,+To] {
  def update(key : A, value : B);

  def result : To;
}
